#include <stdio.h>

int main() {
    int a;
    int b;
    scanf("%d",&a);
    scanf("%d",&b);
    printf("Addition of the numbers is: %d.\n", (a+b));
    printf("Subraction of the numbers is: %d. \n", (a-b));
    printf("Multiplication of the numbers is: %d. \n", (a*b));
    printf("Division of the numbers is: %d.\n", (a/b));
    return 0;
}
